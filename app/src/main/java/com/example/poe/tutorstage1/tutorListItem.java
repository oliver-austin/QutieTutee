package com.example.poe.tutorstage1;

public class tutorListItem {
    private String head;
    private String desc;

    public tutorListItem(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
